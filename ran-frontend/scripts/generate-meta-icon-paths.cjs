const fs = require("fs");
const path = require("path");

const APP_DIR = path.join(__dirname, "..", "app");
const OUTPUT_FILE = path.join(
  __dirname,
  "..",
  "lib",
  "generated",
  "meta-icon-paths.generated.ts",
);

function isRouteGroup(segment) {
  return segment.startsWith("(") && segment.endsWith(")");
}

function isPageFile(filePath) {
  return filePath.endsWith(`${path.sep}page.tsx`);
}

function toRouteFromPageFile(filePath) {
  const rel = path.relative(APP_DIR, filePath);
  const parts = rel.split(path.sep);
  parts.pop(); // page.tsx

  const cleaned = parts.filter(
    (segment) =>
      segment &&
      !isRouteGroup(segment) &&
      !segment.startsWith("@") &&
      segment !== "(.)",
  );

  // Skip notfound catch-all routes from admin lists.
  if (cleaned.some((segment) => segment.toLowerCase().includes("notfound"))) {
    return null;
  }

  const mapped = cleaned.map((segment) => {
    if (segment.startsWith("[") && segment.endsWith("]")) return "*";
    return segment;
  });

  if (mapped.length === 0) return "/";
  return `/${mapped.join("/")}`;
}

function collectPageFiles(dir, out = []) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      collectPageFiles(fullPath, out);
      continue;
    }
    if (entry.isFile() && isPageFile(fullPath)) {
      out.push(fullPath);
    }
  }
  return out;
}

function buildLabel(route) {
  return route === "/" ? "/ (Home)" : route;
}

function generate() {
  const pageFiles = collectPageFiles(APP_DIR);
  const routes = [...new Set(pageFiles.map(toRouteFromPageFile).filter(Boolean))].sort();

  // Keep "/" first for readability.
  routes.sort((a, b) => {
    if (a === "/") return -1;
    if (b === "/") return 1;
    return a.localeCompare(b);
  });

  const lines = [];
  lines.push("// Auto-generated by scripts/generate-meta-icon-paths.cjs");
  lines.push("// Do not edit manually.");
  lines.push("");
  lines.push('export const META_ICON_PATHS: Array<{ path: string; label: string }> = [');
  for (const route of routes) {
    lines.push(`  { path: "${route}", label: "${buildLabel(route)}" },`);
  }
  lines.push("];");
  lines.push("");

  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, lines.join("\n"), "utf8");
  console.log(`[meta-icon-paths] Generated ${routes.length} routes at ${OUTPUT_FILE}`);
}

generate();

